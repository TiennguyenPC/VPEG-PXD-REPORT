import { STORED_VALUES_EN } from './storedValues';

export const LOCALE_STORAGE_KEY = 'shareLocale';
export const DEFAULT_LOCALE = 'vi';
export const SUPPORTED_LOCALES = ['vi', 'en'];

const SORTED_EN_KEYS = Object.keys(STORED_VALUES_EN).sort((a, b) => b.length - a.length);

const MILESTONE_TITLE_KEYS = {
  'PHÁP LÝ': 'scurve.milestone.legal',
  'THIẾT KẾ': 'scurve.milestone.design',
  'VẬT TƯ': 'scurve.milestone.materials',
  'THI CÔNG': 'scurve.milestone.construction',
  'BÀN GIAO HỒ SƠ': 'scurve.milestone.handover',
  KICKOFF: 'scurve.milestone.kickoff',
  COD: 'scurve.milestone.cod',
};

/** Milestone axis labels (PHÁP LÝ → Giấy phép / Permit) */
export function displayMilestoneTitle(title, t, ts) {
  const key = MILESTONE_TITLE_KEYS[String(title || '').toUpperCase()];
  if (key) return t(key);
  return ts(title);
}

/** Translate user-entered / stored Vietnamese labels for guest EN view */
export function translateStored(locale, value) {
  if (value === null || value === undefined || value === '') return value;
  if (locale === 'vi') return value;

  const str = String(value);

  if (STORED_VALUES_EN[str]) return STORED_VALUES_EN[str];

  if (str.includes('\n')) {
    return str.split('\n').map((line) => translateStored(locale, line)).join('\n');
  }

  let result = str;
  for (const key of SORTED_EN_KEYS) {
    if (result.includes(key)) {
      result = result.split(key).join(STORED_VALUES_EN[key]);
    }
  }
  return result;
}

/** Simple `{key}` interpolation for t() results */
export function formatMessage(template, vars = {}) {
  if (!template) return template;
  return Object.entries(vars).reduce(
    (str, [k, v]) => str.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v)),
    template,
  );
}
